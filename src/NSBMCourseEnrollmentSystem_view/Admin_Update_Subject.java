    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
         Subject us =new Subject();
        us.setName(name.getText());
        us.setSubject_code(ID.getText());
        
        if(yes.isSelected()){
            us.setCompulsory(true);
        }
        else {
            us.setCompulsory(false);
        }
        us.setCredits(Integer.parseInt(credit.getText()));
        us.setCourse(course.getSelectedItem().toString());
        us.setYear(Integer.parseInt(year.getText()));
        us.setSemester(Integer.parseInt(sem.getText()));
         
        try {
            Subject_controll.Subject_update(us);
            JOptionPane.showMessageDialog(this,"Updated Success");
        } catch (SQLException ex) {
            Logger.getLogger(Admin_Update_Subject.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_updateActionPerformed

