    private void changeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changeActionPerformed
                String id = stu_id.getText();
                
        try{
            Subject us =Subject_controll.getsub(id);
            if (us!=null){
                ID.setText(us.getSubject_code());
                name.setText(us.getName());
                
                credit.setText(Integer.toString(us.getCredits()));
                
                course.setSelectedItem(us.getCourse());
                year.setText(Integer.toString(us.getYear()));
                
                
                
                sem.setText(Integer.toString(us.getSemester()));
                if(us.isCompulsory()){
                    yes.setSelected(rootPaneCheckingEnabled);
                }
                else{
                    no.setSelected(rootPaneCheckingEnabled);
                }
                
                
                
            }
            else{
                JOptionPane.showMessageDialog(this,"There is no Subject");
            }

        }   catch (HeadlessException | SQLException ex) {
            Logger.getLogger(Admin_Update_Subject.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_changeActionPerformed

